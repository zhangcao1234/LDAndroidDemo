    /**
     * 读取zip文件中制定文件的内容
     *
     * @param zipFile      目标zip文件对象
     * @param readFileName 目标读取文件名字
     * @return 文件内容
     */
    public static String getZipFileContent(String zipFile, String readFileName) {
        StringBuilder content = null;
        try {
            content = new StringBuilder();
            ZipFile zip = new ZipFile(new File(zipFile));
            Enumeration<ZipEntry> entries = (Enumeration<ZipEntry>) zip.entries();

            ZipEntry ze;
            // 枚举zip文件内的文件/
            while (entries.hasMoreElements()) {
                ze = entries.nextElement();
                // 读取目标对象
                if (ze.getName().equals(readFileName)) {
                    Scanner scanner = new Scanner(zip.getInputStream(ze));
                    while (scanner.hasNextLine()) {
                        content.append(scanner.nextLine());
                    }
                    scanner.close();
                }
            }
            zip.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
        return content.toString();
    }

    public static boolean unzip_xapk(String zipFile, String unZipFileName, String targetFilePath, String location) {
        try {
            File fSourceZip = new File(zipFile);
            String targetPath = targetFilePath;
            String fileName = unZipFileName;
            if (location.equals("EXTERNAL_STORAGE")) {
                int index = targetFilePath.lastIndexOf("/");
                targetPath = targetFilePath.substring(0, index + 1);
                fileName = targetFilePath.substring(index + 1, targetFilePath.length());
                targetPath = Environment.getExternalStorageDirectory() + "/" + targetPath;
            }
            File temp = new File(targetPath);
            if (!temp.exists()) {
                Log.i("xapkInstall", "create file " + targetPath + " : " + temp.mkdirs());
            }

            ZipFile zFile = new ZipFile(fSourceZip);
            Enumeration e = zFile.entries();
            while (e.hasMoreElements()) {
                ZipEntry entry = (ZipEntry) e.nextElement();
                if (entry.getName().equals(unZipFileName)) {
                    Log.d("xapkInstall", "Extracting " + targetPath);
                    BufferedInputStream bis = new BufferedInputStream(zFile.getInputStream(entry));
                    byte[] buffer = new byte['Ѐ'];
                    FileOutputStream fos = new FileOutputStream(new File(targetPath + fileName));
                    BufferedOutputStream bos = new BufferedOutputStream(fos, 1024);
                    int b;
                    while ((b = bis.read(buffer, 0, 1024)) != -1) {
                        bos.write(buffer, 0, b);
                    }
                    bos.flush();
                    bos.close();
                    bis.close();
                }
            }
        } catch (IOException ioe) {
            Log.i("xapkInstall", "IOError :" + ioe);
            return false;
        }
        return true;
    }

    public static Map<String, String> analysisManifest(String file) {
        //解析manifest.json
        Map<String, String> map = null;
        try {
            JSONObject jsonObject = new JSONObject(file);
            map = new HashMap<>();
            map.put("package_name", jsonObject.getString("package_name"));
            map.put("version", String.valueOf(jsonObject.getInt("xapk_version")));
            map.put("name", jsonObject.getString("name"));
            map.put("min_sdk_version", jsonObject.getString("min_sdk_version"));
            map.put("target_sdk_version", jsonObject.getString("target_sdk_version"));
            map.put("total_size", String.valueOf(jsonObject.getInt("total_size")));
            JSONArray expansions = jsonObject.getJSONArray("expansions");
            if (expansions.length() > 0) {
                JSONObject expansion = expansions.getJSONObject(0);
                map.put("file", expansion.getString("file"));
                map.put("install_location", expansion.getString("install_location"));
                map.put("install_path", expansion.getString("install_path"));
            }
        } catch (Exception e) {
            Log.i("apkinstaller", "analysisManifest error " + e);
        }
        return map;
    }

    public static boolean clearAllCache(Context context) {
        deleteDir(context.getCacheDir());
        if (Environment.getExternalStorageState().equals(Environment.MEDIA_MOUNTED)) {
            deleteDir(context.getExternalCacheDir());
            return true;
        } else {
            return false;
        }
    }

    private static boolean deleteDir(File dir) {
        if (dir != null && dir.isDirectory()) {
            String[] children = dir.list();
            for (int i = 0; i < children.length; i++) {
                boolean success = deleteDir(new File(dir, children[i]));
                if (!success) {
                    return false;
                }
            }
        }
        return dir.delete();
    }

    //删除指定文件夹下所有文件
//param path 文件夹完整绝对路径
    public static boolean delAllFile(String path) {
        boolean flag = false;
        File file = new File(path);
        if (!file.exists()) {
            return flag;
        }
        if (!file.isDirectory()) {
            return flag;
        }
        String[] tempList = file.list();
        File temp = null;
        for (int i = 0; i < tempList.length; i++) {
            if (path.endsWith(File.separator)) {
                temp = new File(path + tempList[i]);
            } else {
                temp = new File(path + File.separator + tempList[i]);
            }
            if (temp.isFile()) {
                temp.delete();
                flag = true;
            }
            if (temp.isDirectory()) {
                delAllFile(path + "/" + tempList[i]);//先删除文件夹里面的文件
                flag = true;
            }
        }
        return flag;
    }

    /**
     * 从URI获取文件地址
     *
     * @param context 上下文
     * @param uri     文件uri
     */
    public static String getFilePath(Context context, Uri uri) {
        final boolean isKitKat = Build.VERSION.SDK_INT >= Build.VERSION_CODES.KITKAT;
        // DocumentProvider
        if (isKitKat && DocumentsContract.isDocumentUri(context, uri)) {
            // ExternalStorageProvider
            if (isExternalStorageDocument(uri)) {
                final String docId = DocumentsContract.getDocumentId(uri);
                final String[] split = docId.split(":");
                final String type = split[0];

                if ("primary".equalsIgnoreCase(type)) {
                    return Environment.getExternalStorageDirectory() + "/" + split[1];
                }

                // TODO handle non-primary volumes
            }
            // DownloadsProvider
            else if (isDownloadsDocument(uri)) {

                final String id = DocumentsContract.getDocumentId(uri);
                final Uri contentUri = ContentUris.withAppendedId(
                        Uri.parse("content://downloads/public_downloads"), Long.valueOf(id));

                return getDataColumn(context, contentUri, null, null);
            }
            // MediaProvider
            else if (isMediaDocument(uri)) {
                final String docId = DocumentsContract.getDocumentId(uri);
                final String[] split = docId.split(":");
                final String type = split[0];

                Uri contentUri = null;
                if ("image".equals(type)) {
                    contentUri = MediaStore.Images.Media.EXTERNAL_CONTENT_URI;
                } else if ("video".equals(type)) {
                    contentUri = MediaStore.Video.Media.EXTERNAL_CONTENT_URI;
                } else if ("audio".equals(type)) {
                    contentUri = MediaStore.Audio.Media.EXTERNAL_CONTENT_URI;
                }

                final String selection = "_id=?";
                final String[] selectionArgs = new String[]{
                        split[1]
                };

                return getDataColumn(context, contentUri, selection, selectionArgs);
            }
        }
        // MediaStore (and general)
        else if ("content".equalsIgnoreCase(uri.getScheme())) {
            return getDataColumn(context, uri, null, null);
        }
        // File
        else if ("file".equalsIgnoreCase(uri.getScheme())) {
            return uri.getPath();
        }

        return null;
    }


    /**
     * Get the value of the data column for this Uri. This is useful for
     * MediaStore Uris, and other file-based ContentProviders.
     *
     * @param context       The context.
     * @param uri           The Uri to query.
     * @param selection     (Optional) Filter used in the query.
     * @param selectionArgs (Optional) Selection arguments used in the query.
     * @return The value of the _data column, which is typically a file path.
     */
    private static String getDataColumn(Context context, Uri uri, String selection,
                                        String[] selectionArgs) {
        Cursor cursor = null;
        final String column = "_data";
        final String[] projection = {column};
        try {
            cursor = context.getContentResolver().query(uri, projection, selection,
                    selectionArgs, null);
            if (cursor != null && cursor.moveToFirst()) {
                final int index = cursor.getColumnIndexOrThrow(column);
                return cursor.getString(index);
            }
        } finally {
            if (cursor != null)
                cursor.close();
        }
        return null;
    }


